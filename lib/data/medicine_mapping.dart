  // 약 이름으로 카테고리 찾기
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  static String getCategory(String medicineName) {
    // 소문자로 변환해서 비교 (대소문자 무시)
    String lowerName = medicineName.toLowerCase();

    // 1. 정확한 이름 매칭
    for (var key in _medicineCategories.keys) {
      if (lowerName.contains(key.toLowerCase())) {
        print('✅ 매칭: "$medicineName" → ${_medicineCategories[key]}');
        return _medicineCategories[key]!;
      }
    }

    // 2. 성분명 매칭
    for (var key in _ingredientCategories.keys) {
      if (lowerName.contains(key.toLowerCase())) {
        print('✅ 성분 매칭: "$medicineName" → ${_ingredientCategories[key]}');
        return _ingredientCategories[key]!;
      }
    }

    // 3. 못 찾으면 기본값
    print('⚠️ 미분류: "$medicineName" → 처방약');
    return '처방약';
  }

  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  // 여러 약 이름으로 카테고리 추론 (다수결)
  // ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
  static String getCategoryFromList(List<String> medicineNames) {
    if (medicineNames.isEmpty) {
      print('⚠️ 약 목록이 비어있음 → 처방약');
      return '처방약';
    }

    print('\n🔍 약 분류 시작 (${medicineNames.length}개 약)');

    Map<String, int> votes = {};

    for (String name in medicineNames) {
      String category = getCategory(name);
      votes[category] = (votes[category] ?? 0) + 1;
    }

    print('📊 투표 결과: $votes');

    // 가장 많이 나온 카테고리 반환
    String mostVoted = '처방약';
    int maxVotes = 0;

    votes.forEach((category, count) {
      // '처방약'이 아닌 것 중 가장 많은 것
      if (count > maxVotes) {
        if (category != '처방약' || maxVotes == 0) {
          maxVotes = count;
          mostVoted = category;
        }
      }
    });

    print('✅ 최종 결과: $mostVoted ($maxVotes표)\n');
    return mostVoted;
  }

